class CuocGoi {
    private String soThueBao;
    private String khuVuc;
    private int soPhut;
    private long giaTien;

    public CuocGoi(String soThueBao, String khuVuc, int soPhut, long giaTien) {
        this.soThueBao = soThueBao;
        this.khuVuc = khuVuc;
        this.soPhut = soPhut;
        this.giaTien = giaTien;
    }
}
